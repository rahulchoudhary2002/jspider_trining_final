class program10
{
	
	
		static void addition(int a, int b)
		{
			int sum=a+b;
			System.out.println(a+"+"+b+"= "+sum);
		}
		static void sub(int a, int b)
		{
			int sub=a-b;
			System.out.println(a+"-"+b+"= "+sub);
		}
	public static void main(String[] args)
	{
		addition(12,13);
		sub(15,10);
	}
}